# Estrutura FOR
for i in range(1, 6):
    print("estrutura FOR:", i)

# Estrutura WHILE
contadorWhile = 1
while contadorWhile <= 5:
    print("estrutura WHILE:", contadorWhile)
    contadorWhile += 1

# Estrutura DO-WHILE
contadorDoWhile = 1
while True:
    print("estrutura DO-WHILE:", contadorDoWhile)
    contadorDoWhile += 1
    if contadorDoWhile > 5:
        break
